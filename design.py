import turtle as t
t.hideturtle()
t.being_fill()
t.fillcolor("blue")
t.circle(80)
t.end_fill()
t.penup()
t.goto(150,0)
t.pendown()
t.being_fill()
t.fillcolor("red")
t.circle(40)
t.end_fill()
t.penup()
t.goto(150,-120)
t.pendown()
t.being_fill()
t.fillcolor("yellow")
t.circle(50)
t.end_fill()
t.penup()
t.goto(205,95)